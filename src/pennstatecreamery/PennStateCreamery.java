    private void validateEntries() throws SQLException, Exception{
        //Error Check Main Entries Prior to Creating New Customer
        //In the Database
        street = streetNameField.getText().toString();
        city = cityField.getText().toString();
        zipCode = zipField.getText().toString();
        state = stateField.getText().toString();
        firstName = firstNameField.getText().toString();
        lastName = lastNameField.getText().toString();
        phoneNumber = phoneField.getText().toString();
        pinNumber = userPinField.getText().toString();
        streetNum = streetNumField.getText().toString();
        
        if(!firstName.matches("[a-zA-Z]+")){
            firstNameField.setBackground(Color.red);
            JOptionPane.showMessageDialog(null,"Please enter a valid first name entry.","Name Error", JOptionPane.ERROR_MESSAGE);
            firstNameField.setBackground(Color.white);
        }else if(!lastName.matches("[a-zA-Z]+")){
            lastNameField.setBackground(Color.red);
            JOptionPane.showMessageDialog(null,"Please enter a valid  last name entry.","Name Error", JOptionPane.ERROR_MESSAGE);
            lastNameField.setBackground(Color.white);
        }else if(!street.matches("[a-z A-Z]+")){
            streetNameField.setBackground(Color.red);
            JOptionPane.showMessageDialog(null,"Please enter a valid street name.","Address Error", JOptionPane.ERROR_MESSAGE);
            streetNameField.setBackground(Color.white);
        }else if(!phoneNumber.matches("[0-9]{10}")){
            phoneField.setBackground(Color.red);
            JOptionPane.showMessageDialog(null,"Please enter a valid phone number.\nFormat to follow: '5701234567'.","Phone Number Error", JOptionPane.ERROR_MESSAGE);
            phoneField.setBackground(Color.white);
        }else if(!pinNumber.matches("[0-9]{4}")){
            userPinField.setBackground(Color.red);
            JOptionPane.showMessageDialog(null,"Please enter a valid pin number.\nFormat to follow: '1234'.","Pin Error", JOptionPane.ERROR_MESSAGE);
            userPinField.setBackground(Color.white);
        }else if(!city.matches("[a-zA-Z]+")){
            cityField.setBackground(Color.red);
            JOptionPane.showMessageDialog(null,"Please enter a valid city.","City Error", JOptionPane.ERROR_MESSAGE);
            cityField.setBackground(Color.white);
        }else if(!zipCode.matches("[0-9]{5}")){
            zipField.setBackground(Color.red);
            JOptionPane.showMessageDialog(null,"Please enter a valid zip code.\nFormat to follow: '18650'.","Zipcode Error", JOptionPane.ERROR_MESSAGE);
            zipField.setBackground(Color.white);
        }else if(!state.matches("[a-zA-Z]{2}")){
            stateField.setBackground(Color.red);
            JOptionPane.showMessageDialog(null,"Please enter a valid state.\nFormat to follow: 'PA' or 'pa","State Error", JOptionPane.ERROR_MESSAGE);
            stateField.setBackground(Color.white);
        }else if(!streetNum.matches("[0-9]+")){
            streetNumField.setBackground(Color.red);
            JOptionPane.showMessageDialog(null,"Please enter a valid street number.","Address Error", JOptionPane.ERROR_MESSAGE);
            streetNumField.setBackground(Color.white);
        }else{
            //Parse Valid Strings to Int Values
            validZip = Integer.parseInt(zipField.getText());
            //validPhone = Double.parseDouble(phoneField.getText());
            validPin = Integer.parseInt(pinNumber);
            validStreetNum = Integer.parseInt(streetNumField.getText());
            
            //Call to Database to create new customer
            nonExistingPin = db.checkUserPin(validPin);
            
            if(nonExistingPin){
                customer.setCustomerDetails(firstName, lastName, street, validStreetNum,
                    city, state, phoneNumber, validZip, validPin);
                db.storeCustomer(customer);
                JOptionPane.showMessageDialog(null,"Customer Successfully Created!","Success", JOptionPane.INFORMATION_MESSAGE);

                setVisible(false);
                OrderFrame orderFrame = new OrderFrame(db, dbV, co, customer);

                orderFrame.setVisible(true);
            }
        }        
    }
    
