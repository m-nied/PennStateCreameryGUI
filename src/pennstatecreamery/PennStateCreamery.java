    private void existingLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_existingLoginActionPerformed
        pinNumber = existingPinField.getText().toString();
        
        //Error Check Main Entries Prior to Creating New Customer
        //In the Database        
        if(!pinNumber.matches("[0-9]{4}")){
            existingPinField.setBackground(Color.red);
            JOptionPane.showMessageDialog(null,"Please enter a valid pin number.","Pin Error", JOptionPane.ERROR_MESSAGE);
            existingPinField.setBackground(Color.white);
        }else{
            //Parse userPin
            validPin = Integer.parseInt(pinNumber);
            //Pull Existing Customer From Database
            
            isExisting = db.existingCustomer(customer, pinNumber);
            
            if(isExisting){
                customer.setUserPin(validPin);
                setVisible(false);
                OrderFrame orderFrame = new OrderFrame(db, dbV, co, customer);

                orderFrame.setVisible(true);
            }
        }
    }//GEN-LAST:event_existingLoginActionPerformed

